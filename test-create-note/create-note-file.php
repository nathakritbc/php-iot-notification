<?php
// ชื่อของไฟล์ใหม่ที่คุณต้องการสร้าง
$newFileName = 'newfile.php';

// เนื้อหา (โค้ด) ที่คุณต้องการใส่ในไฟล์
$code = '<?php echo "Hello, World!"; ?>';

// สร้างไฟล์และเขียนโค้ดลงในไฟล์
if (file_put_contents($newFileName, $code) !== false) {
echo "ไฟล์ถูกสร้างและโค้ดถูกเขียนลงไฟล์เรียบร้อยแล้ว.";
} else {
echo "เกิดข้อผิดพลาดในการสร้างไฟล์หรือเขียนโค้ด.";
}
?>